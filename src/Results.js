import React from 'react';
import Pet from './Pet';

const Results = ({ pets }) => {
	return (
		<div className="search">
			{!pets.length ? (
				<h1>No Pets Found</h1>
			) : (
				pets.map(pet => {
					let breed;
					if (Array.isArray(pet.breeds.breed)) {
						breed = pet.breeds.breed.join(', ');
					} else {
						breed = pet.breeds.breed;
					}
					return (
						<Pet
							animal={pet.animal}
							key={pet.id}
							name={pet.name}
							breed={breed}
							media={pet.media}
							location={`${pet.contact.city}, ${pet.contact.state}`}
							id={pet.id}
						/>
					);
				})
			)}
		</div>
	);
};

export default Results;
